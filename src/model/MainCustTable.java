package model;

/**
 *
 * @author ajw51
 */
public class MainCustTable {
    
    String name, address, post, phone;
    int id;

    public MainCustTable(int id, String name, String address, String post, String phone) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.post = post;
        this.phone = phone;
    }
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
    
}
